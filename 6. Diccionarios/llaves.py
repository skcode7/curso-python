diccionario = {"a":1, "b":2, "c":3, "d":21, "e":81, "f":5, "g":32}
resultado = tuple(diccionario.keys())
print(resultado)

#Similar para obtener los valores
resultado = tuple(diccionario.values())
print(resultado)

#Para obtener todos los items
resultado = tuple(diccionario.items())
print(resultado)